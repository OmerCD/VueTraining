<template>
  <section>
    <coach-filter @change-filter='setFilters'></coach-filter>
  </section>
  <section>
    <base-card>
      <div class='controls'>
        <base-button mode='outline'>Refresh</base-button>
        <base-button link to='/register'>Register</base-button>
      </div>
      <ul v-if='hasCoaches'>
        <coach-item v-for='coach in filteredCoaches'
                    :key='coach.id'
                    :id='coach.id'
                    :areas='coach.areas'
                    :first-name='coach.firstName'
                    :last-name='coach.lastName'
                    :rate='coach.hourlyRate'
        ></coach-item>
      </ul>
      <h3 v-else>No Coaches</h3>
    </base-card>
  </section>
</template>

<script>
import CoachItem from '@/components/coaches/CoachItem';
import BaseCard from '@/components/ui/BaseCard';
import BaseButton from '@/components/ui/BaseButton';
import CoachFilter from '@/components/coaches/CoachFilter';

export default {
  components: { CoachFilter, BaseButton, BaseCard, CoachItem },
  computed: {
    filteredCoaches() {
      const coaches = this.$store.getters['coaches/coaches'];
      return coaches.filter(coach => {
        if(this.activeFilters.frontend && coach.areas.includes('frontend')) return true;
        if(this.activeFilters.backend && coach.areas.includes('backend')) return true;
        if(this.activeFilters.career && coach.areas.includes('career')) return true;
        return false;
      })
    },
    hasCoaches() {
      return this.$store.getters['coaches/hasCoaches'];
    }
  },
  name: 'CoachesList',
  data(){
    return{
      activeFilters:{
        frontend: true,
        backend: true,
        career: true
      }
    }
  },
  methods:{
    setFilters(updatedFilters){
      this.activeFilters = updatedFilters;
    }
  }
};
</script>

<style scoped>
ul {
  list-style: none;
  margin: 0;
  padding: 0;
}

.controls {
  display: flex;
  justify-content: space-between;
}
</style>